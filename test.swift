// 変数(in)
var name: String = "komorebi"
var num: Int = 100
var float: Float = 1.23456789
var double: Double = 1.23456789
var good: Bool = true
var bad: Bool = false

// 出力(put)
print(name)
print(num)
print(float)
print(double)
print(good)
print(bad)

// 演算(+,-,*,/,%)
print(1+1)
print(10-6)
print(2*5)
print(10/2)
print(10%3)

// StringとIntの違い
print(1+1)
print("1"+"1")

// 定数(let)
let TEXT: String = "Hello"
let NUM: Int = 500

// 出力(put)
print(TEXT)
print(NUM)

// if/else文
if 20==NUM {
print(NUM)
}
else if 20<=NUM {
print(TEXT)
}
else {
print(TEXT)
}
